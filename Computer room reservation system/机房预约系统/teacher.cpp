//查看所有预约
void Teacher::showAllOrder() {
	OrdeFile of;
	if (of.m_Size == 0) {
		cout << "记录为空" << endl;
		system("Pause");
		system("cls");
		return;
	}
	for (int i = 0; i < of.m_Size; i++) {
		cout << "第" << i + 1 << "条数据如下: " << endl;
		cout << "预约日期:  周" << of.m_orderData[i]["data"] << " ";
		cout << "时间段: " << (of.m_orderData[i]["interval"] == "1" ? "上午" : "下午") << " ";
		cout << "学号: " << of.m_orderData[i]["stuId"] << " ";
		cout << "姓名: " << of.m_orderData[i]["stuName"] << " ";
		cout << "机房编号: " << of.m_orderData[i]["room"] << " ";
		string status = "审核状态: ";
		if (of.m_orderData[i]["status"] == "1") {
			status += "审核中!";
		}
		else if (of.m_orderData[i]["status"] == "2") {
			status += "预约成功!";
		}
		else if (of.m_orderData[i]["status"] == "-1") {
			status += "预约失败!";
		}
		else {
			status += "预约已取消";
		}
		cout << status << endl;
	}
	system("pause");
	system("cls");

}

//审核预约
void Teacher::validOeder() {
	OrdeFile of;
	if (of.m_Size == 0) {
		cout << "记录为空" << endl;
		system("pause");
		system("cls");
		return;
	}
	cout << "待审核的预约记录如下: " << endl;
	vector<int>v;
	int index = 0;
	for (int i = 0; i < of.m_Size; i++) {
		if (of.m_orderData[i]["status"] == "1") {
			v.push_back(i);
			cout << ++index << " . " << endl;
			cout << "预约日期:  周" << of.m_orderData[i]["data"] << " ";
			cout << "时间段: " << (of.m_orderData[i]["interval"] == "1" ? "上午" : "下午") << " ";
			cout << "学号: " << of.m_orderData[i]["stuId"] << " ";
			cout << "姓名: " << of.m_orderData[i]["stuName"] << " ";
			cout << "机房编号: " << of.m_orderData[i]["room"] << " ";
			string status = "状态: 审核中";
			cout << status << endl;
		}
	}
	cout << "请输入要审核的预约记录 0代表返回" << endl;
	int select = 0;//选择的条数
	int ret = 0;//选择通过不通过
	while (true) {
		cin >> select;
		if (select >= 0 && select <= v.size()) {
			if (select == 0) {
				break;
			}
			else {
				cout << "请选择通过或未通过" << endl;
				cout << "1.通过" << endl;
				cout << "2.不通过" << endl;
				cin >> ret;
				if (ret == 1) {
					of.m_orderData[v[select - 1]]["status"] = "2";
				}
				else if (ret == 2)
				{
					of.m_orderData[v[select - 1]]["status"] = "-1";
				}
				of.updateOrder();
				cout << "审核完毕" << endl;
				break;
			}

		}
		else {
			cout << "输入有误 重新输入" << endl;
			break;
		}
	}
	system("pause");
	system("cls");
}